package Assignments;

public class Natural_numbers {
    public static void main(String[] args) {
        System.out.println("This is a program to display first 10 natural numbers.");
        int i = 0;
        int k = 0;
        while (i<=10){
            System.out.println(i);
            i++;
        }
        do {
            System.out.println(k);
            k++;
        }while (k<=10);


        for (int j = 0; j <= 10; j++) {
            System.out.println(j);
        }
    }
}